-- 1. Profiles Table
-- Purpose: Holds user profile information. Automatically populated when a new user signs up.
create table public.profiles (
  id uuid references auth.users on delete cascade primary key,
  username text unique not null,
  display_name text,
  avatar_url text,
  is_moderator boolean default false,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Future compatibility columns for Profiles table:
-- bio text
-- profile_picture text
-- career_interests text[]
-- education_details jsonb
-- user_preferences jsonb

-- 2. Communities Table
-- Purpose: Stores community categories (e.g. Medical, Biotech, Computing). Supports extensible banners, rules, etc.
create table public.communities (
  id bigint generated by default as identity primary key,
  name text not null,
  slug text unique not null,
  description text,
  icon text,
  banner_image text,
  color text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null,
  is_active boolean default true not null
);

-- Future compatibility columns for Communities table:
-- rules text[]
-- rules_description text
-- statistics jsonb

-- 3. Community Posts Table
-- Purpose: Stores forum discussion threads. Relational link to profiles (author) and communities.
create table public.community_posts (
  id bigint generated by default as identity primary key,
  author_id uuid references public.profiles(id) on delete cascade not null,
  community_id bigint references public.communities(id) on delete cascade not null,
  title text not null,
  content text not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Future compatibility columns for Community Posts table:
-- slug text
-- edited_at timestamp with time zone
-- is_pinned boolean default false
-- is_locked boolean default false
-- view_count int default 0
-- report_count int default 0

-- 4. Post Reactions Table (Likes/Upvotes)
-- Purpose: Tracks post reactions. Unique constraint ensures one reaction per user per post.
create table public.post_reactions (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) on delete cascade not null,
  post_id bigint references public.community_posts(id) on delete cascade not null,
  reaction_type text default 'upvote' not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  constraint unique_user_post_reaction unique (user_id, post_id)
);

-- 5. Post Comments Table (Replies)
-- Purpose: Holds discussion replies linked to posts and profile authors.
create table public.post_comments (
  id bigint generated by default as identity primary key,
  post_id bigint references public.community_posts(id) on delete cascade not null,
  author_id uuid references public.profiles(id) on delete cascade not null,
  content text not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Future compatibility columns for Post Comments table:
-- parent_comment_id bigint references public.post_comments(id) on delete cascade (for nested threads)
-- upvote_count int default 0

