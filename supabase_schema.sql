alter table public.post_comments enable row level security;

-- Profiles Policies
create policy "Profiles are viewable by everyone" on public.profiles
  for select using (true);

create policy "Profiles can be created by trigger function" on public.profiles
  for insert with check (true);

create policy "Users can update their own profile" on public.profiles
  for update using (auth.uid() = id);

-- Communities Policies
create policy "Communities are viewable by everyone" on public.communities
  for select using (true);

-- Note: Insert/Update/Delete operations for communities table are omitted.
-- Only database admins or moderators (via service_role or specific roles) can create/modify communities.

-- Community Posts Policies
create policy "Posts are viewable by everyone" on public.community_posts
  for select using (true);

create policy "Authenticated users can create posts" on public.community_posts
  for insert with check (auth.role() = 'authenticated' and author_id = auth.uid());

create policy "Authors can edit their own posts" on public.community_posts
  for update using (author_id = auth.uid());

create policy "Authors can delete their own posts" on public.community_posts
  for delete using (author_id = auth.uid());

-- Post Reactions Policies
create policy "Reactions are viewable by everyone" on public.post_reactions
  for select using (true);

create policy "Authenticated users can react to posts" on public.post_reactions
  for insert with check (auth.role() = 'authenticated' and user_id = auth.uid());

create policy "Users can remove their own reactions" on public.post_reactions
  for delete using (user_id = auth.uid());

-- Post Comments Policies
create policy "Comments are viewable by everyone" on public.post_comments
  for select using (true);

create policy "Authenticated users can add comments" on public.post_comments
  for insert with check (auth.role() = 'authenticated' and author_id = auth.uid());

create policy "Authors can edit their own comments" on public.post_comments
  for update using (author_id = auth.uid());

create policy "Authors can delete their own comments" on public.post_comments
  for delete using (author_id = auth.uid());

-- ─────────────────────────────────────────────────────────────
-- PROFILE CREATION TRIGGER
-- ─────────────────────────────────────────────────────────────

-- Function to handle new user signup
create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer set search_path = public
as $$
begin
  insert into public.profiles (id, username, display_name, is_moderator)
  values (
    new.id,
    coalesce(new.raw_user_meta_data->>'username', 'user_' || substr(new.id::text, 1, 8)),
    coalesce(new.raw_user_meta_data->>'username', 'User ' || substr(new.id::text, 1, 8)),
    false
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

-- Trigger definition
create or replace trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

